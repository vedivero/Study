/*
(1)접근제한자 : public > private > protected / default
				= 클래스나 메서드에 접근할 수 있는 범위를 지정-->접근제한자,접근제어자

(2)클래스선언 : class
				= 객체를 만들어 내는, 생성 하는 틀, 프레임, 공장

(3)클래스이름 : 카멜케이스

			ex) Camel / Java100

(4)메서드이름 : 메서드 = 함수 = 어떤 특정한 동작이나 작업, 행위를 수행

			ex)붕어빵 생성 메서드 (팥,밀가루)
		

*/